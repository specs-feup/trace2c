package pt.up.fe.specs.utils;

public class VarAndDim {

    private String var;
    private int dim;

    public VarAndDim(String var, int dim) {
        this.var = var;
        this.dim = dim;
    }

    public String getVar() {
        return var;
    }

    public void setVar(String var) {
        this.var = var;
    }

    public int getDim() {
        return dim;
    }

    public void setDim(int dim) {
        this.dim = dim;
    }
}
